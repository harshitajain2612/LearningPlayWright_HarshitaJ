let x = 10;

x = x+10;

x+= 10; // x = x + 10

x-= 10; // x = x - 10

x *= 2 ; // x = x * 2

x /= 2; // x = x / 2

x %= 3; // x = x % 3

x **= 2; // x = x ** 2 (x raised to the power of 2)

console.log(x); // Output: 1 (the final value of x after all the operations)

