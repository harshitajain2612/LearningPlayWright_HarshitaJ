// && - AND Gate
// OR - OR Gate
// NOT - NOT Gate


let a = true;
let b = false;
console.log(a && b); // Output: false (AND gate returns true only if both inputs are true)

console.log(a || b); // Output: true (OR gate returns true if at least one input is true)   

console.log(!a); // Output: false (NOT gate returns the opposite of the input, so it returns false when the input is true)

console.log(!b); // Output: true (NOT gate returns the opposite of the input, so it returns true when the input is false)
